BAB II
KEGIATAN DALAM TAHUN PELAJARAN


A.    KEGIATAN AWAL TAHUN PELAJARAN 2017/2018
1-     Merencanakan kebutuhan guru setiap mata pelajaran
2-     Merencanakan/membuat pembagian tugas mengajar
3-     Menyusun Program pengajaran, jadwal pelajaran dan kalender pendidikan
4-     Menyusun kebutuhan buku pelajaran, buku pegangan guru
5-     Menyusun kelengkapan alat pelajaran dan bahan pelajaran
6-     Mengadakan rapat

B.     KEGIATAN HARIAN
1.      Memeriksa daftar hadir guru dan pegawai;
2.      Memeriksa dan mengatur kegiatan Pra KBM (berbaris dan membaca do’a);
3.      Memeriksa Silabus dan RPP;
4.      Menyelesaikan surat, menerima dan melaksanakan pekerjaan kantor lainnya;
5.      Mengatasi hambatan yang terjadi dalam proses pembelajaran;
6.      Menghadiri rapat dinas (bila ada undangan);
7.      Mengontrol pelaksanaan kebersihan madrasah dan kelas;
8.      Mengontrol pelaksanaan Imtaq/Diniyah.

C.    KEGIATAN MINGGUAN
- Melaksanakan apel bendera setiap hari Senin dan mengatur pembina upacara secara bergiliran dari kepala madrasah, wakil kepala madrasah dan guru-guru lainnya;
- Melaksanakan supervisi kelas;
- Melaksanakan jumat bersih (jika diperlukan);    
- Memeriksa agenda surat dan menyelesaikan surat keluar masuk;      
- Mengadakan rapat singkat (kilat) setiap hari Sabtu untuk mengevaluasi proses pembelajaran (jika diperlukan).

D.    KEGIATAN BULANAN
Pada awal bulan perlu disusun kegiatan sebagai berikut:
1. Melaksanakan pembayaran honor tenaga pendidik dan kependidikan;
2. Mengadakan pemeriksaan umum:
a.    Daftar hadir siswa;
b.    Daftar hadir guru dan pegawai;
c.    Administrasi guru;
d.   Memeriksa laporan bulanan.

E.     KEGIATAN SEMETER
1.    Menetapkan jadwal ulangan umum;
2.    Mengambil tes ulangan umum;
3.    Melaksanakan ulangan umum;
4.    Melaksanakan pemeriksaan ulangan umum;
5.    Merekap nilai ulangan umum;
6.    Mengisi raport;
7.    Pembagian raport;
8.    Mengisi/memindahkan nilai raport ke buku induk.

F.     KEGIATAN AKHIR TAHUN PELAJARAN 2017/2018
Setiap akhir tahun pelajaran perlu dilaksanakan kegiatan dalam rangka penutupan Tahun Pelajaran sekaligus melaksanakan kegiatan Tahun Pelajaran yang akan datang, antara lain:
1.      Menyelenggarakan kegiatan buku inventaris dan keuangan
2.      Menyelenggarakan kegiatan UN/UAM/UAM-BM
3.      Menyelenggarakan kegiatan kenaikan kelas yang meliputi:
a.       Mengadakan rapat guru
b.      Membentuk panitia ujian semester dan UN/UAM/UAM-BM
c.       Melaksanakan ulangan umum
d.      Pengisian nilai
e.       Pengisian raport
f.       Penulisan ijazah
g.      Pengisian leger/buku induk
h.      Penentuan kelulusan
i.        Penentuan kenaikan kelas
j.        Pembagian raport
k.      Pembagian ijazah

4.      Merencanakan penyusunan anggaran dan program kerja untuk tahun yang akan datang
5. Melaksanakan laporan lengkap akhir Tahun Pelajaran termasuk laporan lengkap UN/UAM/UAM-BN dan lain-lain


